#include "philo.h"

int check_arg(int argc, char **args)
{
    int i;
    int j;

    i = 0;
    j = 1;
    while (j < argc)
    {
        while (args[j][i])
        {
            if (ft_isdigit(args[j][i]) == 0)
                return (0);
            i++;
        }
        i = 0;
        j++;
    }
    return (1);
}